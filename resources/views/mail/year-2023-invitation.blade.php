# Lieve Nolanders,

In 2023 gaat Nolands voor de 4e keer gebeuren. Wederom heet Frans ons welkom op zijn prachtige kampeerboerderij en gaan we er een onvergetelijk weekend van maken. In de Whatsapp-groep Berichtencentrum Nolands is al wat info bekend geworden.

Via onderstaande link kan er aangemeld worden.

Ben jij eerder op Nolands geweest?
Nadat je hebt aangemeld (via dezelfde login als vorig jaar) heb je de mogelijkheid om te betalen. Op het moment dat je aan deze betaling voldaan heb, ben jij een Nolander ‘23! Vergeet alsjeblieft niet je voorkeur in te vullen voor een team. Deadline voor aanmelding én betaling: 13 januari!

Ken jij iemand of ben jij iemand die graag voor het eerst naar Nolands wilt?
Ook jij kan je aanmelden, we kunnen niet met 100% zekerheid zeggen dat er ook genoeg plek is. Nu hoor ik je denken, hoe zit dat dan? We wachten in eerste instantie af hoe veel Nolanders van de afgelopen jaren zich weer aanmelden. Afhankelijk daarvan hebben we nog plek over. 13 januari is de deadline voor het inschrijven, hierna maken we de balans op. Als er plek over is dan krijgen nieuwe, al aangemelde, Nolanders, bericht of ze er alsnog bij kunnen zijn of op de reservelijst terecht komen.

Heb je nog vragen, schroom niet ons te contacten. Wij zijn best lief.

